    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ProdutoModelo prod = new ProdutoModelo();
        prod.setCodigoDeBarras(codigo.getText());
        prod.setEstoque(Integer.parseInt(quantidade.getText()));
        prod.setNome(nome.getText());
        prod.setPreco(Double.parseDouble(preco.getText()));
        ProdutoPersistencia persistencia = new ProdutoPersistencia();
        persistencia.salvarProduto(prod);
    }//GEN-LAST:event_jButton1ActionPerformed

